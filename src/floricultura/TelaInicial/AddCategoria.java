    private void NomeCategoriaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NomeCategoriaKeyTyped
        // TODO add your handling code here:
        char keyPress = evt.getKeyChar();
        if(!Character.isAlphabetic(keyPress) && keyPress != ' '){
            evt.consume();
        }

    }//GEN-LAST:event_NomeCategoriaKeyTyped

    private void CadastrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarBtnActionPerformed
        // TODO add your handling code here:
        String[] options = {"Sim", "Não"};
        int resposta = JOptionPane.showOptionDialog(
            this, 
            "Categoria Registrada!"+ "\n" +  
            "Nome da Categoria: " + NomeCategoria.getText() + "\n\n\n" +
            "Cadastrar nova SubCategoria?" +"\n",
            "Confirmação de Cadastro",
            JOptionPane.DEFAULT_OPTION,
            JOptionPane.WARNING_MESSAGE,
            null,
            options,
            options[0]);
        if (resposta == 1) {
            this.dispose();
        }else{
            NomeCategoria.setText("");
        }
    }//GEN-LAST:event_CadastrarBtnActionPerformed

    private void VoltarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VoltarBtnActionPerformed
        // TODO add your handling code here:
        String[] options = {"Sim", "Cancelar"};
        int resposta = JOptionPane.showOptionDialog(
            this,
            "Esta ação apagará todos os campos, tem certeza?",
            "Confirmação",
            JOptionPane.DEFAULT_OPTION,
            JOptionPane.WARNING_MESSAGE,
            null,
            options,
            options[0]
        );
        if (resposta == 0) {
            this.dispose();
        } else {
            VoltarBtn.requestFocus();
        }
    }//GEN-LAST:event_VoltarBtnActionPerformed

