//Guarda po ID e o NOME dos operadores
public class NomeIDOperadorModel {
    private int id;
    private String nome;

    public NomeIDOperadorModel(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    @Override
    public String toString() {
        return nome; 
    }
}

